//====연산자 정리===============================================================
// 1. 단항연산자 : 부호연산자(+,-), 증감연산자(++,--), 논리연산자(!)
// 1-1 . 부호연산자 : +,-
// 1-2 . 증감연산자 : ++,--
//   - 전위연산자 : ++a , --a (먼저 증가/감소 후 대입)
//   - 후위연산자 : a++ , a-- (먼저 대입 후 증가/감소)
// 1-3 . 논리연산자 : !(not)
// 2. 이항연산자 : 산술연산자(+,-,*,/,%), 비교연산자(>,<,>=,<=,==,!=), 논리연산자(&&,||), 대입연산자(=,+=,-=,*=,/=,%=)
// 2-1 . 산술연산자 : +:더하기, -:빼기, *:곱하기 , /: 나누기, % :나머지
// 2-1-1 ex)
//   int a = 10;
//   int b = 3;
//   System.out.println(a + b); // 13
//   System.out.println(a - b); // 7
//   System.out.println(a * b); // 30
//   System.out.println(a / b); // 3
//   System.out.println(a % b); // 1
// 2-2 . 비교연산자 : > : 작다 , < : 크다 , >= : 작거나같다 , <= :크거나 같다 ,== : 같다 ,!= : 같지않다
// 2-2-1 ex)
//   int a = 10;
//   int b = 3;
//   System.out.println(a > b);  // true
//   System.out.println(a < b);  // false
//   System.out.println(a >= b); // true
//   System.out.println(a <= b); // false
//   System.out.println(a == b); // false
//   System.out.println(a != b); // true
// 2-3 . 논리연산자 : &&(and), ||(or)
// 2-3-1 ex)
//   int a = 10;
//   int b = 3;
//   System.out.println((a > 5) && (b < 5)); // true && true -> true
//   System.out.println((a > 5) || (b > 5)); // true || false -> true
// 2-4 . 대입연산자 : =  , +=  , -=  , *=  , /=  , %= 
// 2-4-1 ex)
//		a = 10;  // a에 10 대입
//		a += 5;  // a에 5를 더한값 대입 (a = a + 5)
//		a -= 3;  // a에 3을 뺀값 대입 (a = a - 3)
//		a *= 2;  // a에 2를 곱한값 대입 (a = a * 2)
//		a /= 4;  // a에 4를 나눈값 대입 (a = a / 4)
//		a %= 3;  // a에 3으로 나눈 나머지값 대입 (a = a % 3)
// 3. 삼항연산자 : (조건) ? 참일때값 : 거짓일때값
// 3-1 . ex)
//   int a = 10;
//   int b = 3;
//   int max = (a > b) ? a : b; // a가 b보다 크면 a를 max에 대입, 아니면 b를 max에 대입
//   System.out.println("max : " + max); // max : 10
// 4. 연산자 우선순위
// - 1순위 : (), ++, --
// - 2순위 : *, /, %
// - 3순위 : +, -
// - 4순위 : >, <, >=, <=
// - 5순위 : ==, !=
// - 6순위 : &&
// - 7순위 : ||
