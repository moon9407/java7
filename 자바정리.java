//====연산자 정리===============================================================
// 1. 단항연산자 : 부호연산자(+,-), 증감연산자(++,--), 논리연산자(!)
// 1-1 . 부호연산자 : +,-
// 1-2 . 증감연산자 : ++,--
//   - 전위연산자 : ++a , --a (먼저 증가/감소 후 대입)
//   - 후위연산자 : a++ , a-- (먼저 대입 후 증가/감소)
// 1-3 . 논리연산자 : !(not)
// 2. 이항연산자 : 산술연산자(+,-,*,/,%), 비교연산자(>,<,>=,<=,==,!=), 논리연산자(&&,||), 대입연산자(=,+=,-=,*=,/=,%=)
// 2-1 . 산술연산자 : +,-,*,/,%
// 2-1-1 ex)
//   int a = 10;
//   int b = 3;
//   System.out.println(a + b); // 13
//   System.out.println(a - b); // 7
//   System.out.println(a * b); // 30
//   System.out.println(a / b); // 3
//   System.out.println(a % b); // 1
// 2-2 . 비교연산자 : >,<,>=,<=,==,!=
// 2-2-1 ex)
//   int a = 10;
//   int b = 3;
//   System.out.println(a > b);  // true
//   System.out.println(a < b);  // false
//   System.out.println(a >= b); // true
//   System.out.println(a <= b); // false
//   System.out.println(a == b); // false
//   System.out.println(a != b); // true
// 2-3 . 논리연산자 : &&(and), ||(or)
// 2-3-1 ex)
//   int a = 10;
//   int b = 3;
//   System.out.println((a > 5) && (b < 5)); // true && true -> true
//   System.out.println((a > 5) || (b > 5)); // true || false -> true
// 2-4 . 대입연산자 : =,+=,-=,*=,/=,%=
// 2-4-1 ex)
//   int a = 10;
//   a += 5; // a = a + 5;
//   System.out.println(a); // 15
// 3. 삼항연산자 : (조건) ? 참일때값 : 거짓일때값
// 4. 연산자 우선순위
// - 1순위 : (), ++, --
// - 2순위 : *, /, %
// - 3순위 : +, -
// - 4순위 : >, <, >=, <=
// - 5순위 : ==, !=
// - 6순위 : &&
// - 7순위 : ||
//=============================================================================

